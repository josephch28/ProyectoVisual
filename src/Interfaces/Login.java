    private void escalarIconoExistente() {
        // Obtiene el ImageIcon actual
        ImageIcon iconoOriginal = (ImageIcon) lblImagen.getIcon();

        if (iconoOriginal != null) {
            // Obtiene la imagen original
            Image imagenOriginal = iconoOriginal.getImage();

            // Escala la imagen al tama√±o del JLabel
            Image imagenEscalada = imagenOriginal.getScaledInstance(
                    lblImagen.getWidth(),
                    lblImagen.getHeight(),
                    Image.SCALE_SMOOTH
            );

            // Reemplaza el icono por la imagen escalada
            lblImagen.setIcon(new ImageIcon(imagenEscalada));
        }
    }
    
    private void configurarCampos() {
    // --- Campo Usuario ---
    txtUsuario.addFocusListener(new java.awt.event.FocusAdapter() {
        @Override
        public void focusGained(java.awt.event.FocusEvent evt) {
            if (txtUsuario.getText().equals("Usuario")) {
                txtUsuario.setText("");
                txtUsuario.setForeground(new java.awt.Color(0, 0, 0)); // texto negro
            }
        }

        @Override
        public void focusLost(java.awt.event.FocusEvent evt) {
            if (txtUsuario.getText().trim().isEmpty()) {
                txtUsuario.setText("Usuario");
                txtUsuario.setForeground(new java.awt.Color(102, 102, 102)); // gris
            }
        }
    });

    // --- Campo Password ---
    txtPassword.addFocusListener(new java.awt.event.FocusAdapter() {
        @Override
        public void focusGained(java.awt.event.FocusEvent evt) {
            String contrasena = new String(txtPassword.getPassword());
            if (contrasena.equals("*******")) {
                txtPassword.setText("");
                txtPassword.setForeground(new java.awt.Color(0, 0, 0));
            }
        }

        @Override
        public void focusLost(java.awt.event.FocusEvent evt) {
            String contrasena = new String(txtPassword.getPassword());
            if (contrasena.trim().isEmpty()) {
                txtPassword.setText("*******");
                txtPassword.setForeground(new java.awt.Color(102, 102, 102));
            }
        }
    });
}

